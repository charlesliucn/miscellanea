function A=izigzag(B)
%,函数izigzag，将zigzag扫描得到的64元素向量恢复为Zig-Zag扫描之前的8*8矩阵
%,输入变量：	1*64的矢量
%,返回值：   Zig-Zag扫描之前的8*8矩阵

	len=length(B);  %获取输入矩阵大小
    A=zeros(8);
	if(len==64)  %满足矩阵为8*8的要求
        %设定扫描顺序与矩阵的对应关系
        zigzag_index=[  1, 2, 6, 7, 15,16,28,29;
                        3, 5, 8, 14,17,27,30,43;
                        4, 9, 13,18,26,31,42,44;
                        10,12,19,25,32,41,45,54;
                        11,20,24,33,40,46,53,55;
                        21,23,34,39,47,52,56,61;
                        22,35,38,48,51,57,60,62;
                        36,37,49,50,58,59,63,64;];
        else error('函数的输入不符合要求!\n要求输入为64元素的向量');   %输入不符合要求
	end
	for i=1:8
        for j=1:8
            A(i,j)=B(zigzag_index(i,j));	
        end
    end
end